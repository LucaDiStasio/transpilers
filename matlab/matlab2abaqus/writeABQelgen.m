fileId = fopen(filepath, 'a');

fprintf(fileId,'**\n');

rows = size(elements,1);
columns = size(elements,2);

fprintf(fileId,strcat('*ELEMENT, TYPE = ',type,', ELSET = ',elset,'\n'));
for i=1:rows
    line = strcat(' ',num2str(E0+(i-1)*deltaE));
    for j=1:columns
        line = strcat(line,', ',num2str(elements(i,j)));
    end
    fprintf(fileId,strcat(line,'\n'));
end

